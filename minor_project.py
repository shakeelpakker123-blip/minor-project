import numpy as np

def get_marks():
    n = int(input("Enter number of students: "))
    marks = []

    for i in range(n):
        m = float(input(f"Enter marks of student {i+1}: "))
        marks.append(m)

    return np.array(marks)

def analyze_marks(arr):
    total = arr.sum()
    average = arr.mean()
    highest = arr.max()
    lowest = arr.min()

    print("\n----- Marks Analysis -----")
    print("Total Marks:", total)
    print("Average Marks:", average)
    print("Highest Marks:", highest)
    print("Lowest Marks:", lowest)

    if average >= 85:
        grade = "A"
    elif average >= 70:
        grade = "B"
    elif average >= 50:
        grade = "C"
    else:
        grade = "Fail"

    print("Grade:", grade)

# Main Program
print("STUDENT MARKS ANALYZER")

marks_array = get_marks()
analyze_marks(marks_array)

print("\nThank you!")

