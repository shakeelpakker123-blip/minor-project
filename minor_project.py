import numpy as np

def get_expenses():
    n = int(input("Enter number of days: "))
    expenses = []

    for i in range(n):
        amt = float(input(f"Enter expense for day {i+1}: "))
        expenses.append(amt)

    return np.array(expenses)

def analyze_expenses(arr):
    total = arr.sum()
    average = arr.mean()
    highest = arr.max()

    print("\n----- Expense Summary -----")
    print("Total Expense:", total)
    print("Average Expense:", average)
    print("Highest Expense:", highest)

    if average > 1000:
        print("Warning: Your average expense is very high!")

# Main Program
print("DAILY EXPENSE TRACKER")

expense_array = get_expenses()
analyze_expenses(expense_array)

print("\nThank you for using Daily Expense Tracker!")

